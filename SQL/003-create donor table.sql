CREATE TABLE DONOR ( 
D_CODE      INTEGER IDENTITY (1,1)     NOT NULL UNIQUE, 
D_FNAME     VARCHAR(35) NOT NULL, 
D_LNAME     VARCHAR(35) NOT NULL, 
D_ADDRESS	VARCHAR(50) NULL, 
D_CITY		VARCHAR(35) NULL, 
D_STATE		VARCHAR(35) NULL,
D_ZIP		CHAR(10) 	NULL,
D_COUNTY	VARCHAR(35)	NULL,
D_BIRTHDAY	CHAR(15)		NULL,
D_GENDER	VARCHAR(10) NULL,
D_CELL_PHONE    CHAR(10) NULL,     
D_HOME_PHONE    CHAR(10) NULL,     
D_WORK_PHONE    CHAR(10) NULL,     
D_EMAIL  		VARCHAR(50) NULL, 
D_CO_NAME       VARCHAR(50) NULL, 
D_POSITION      VARCHAR(50) NULL, 
D_CO_ADDRESS    VARCHAR(50) NULL, 
D_CO_CITY       VARCHAR(35) NULL, 
D_CO_STATE   	VARCHAR(20)     NULL, 
D_CO_ZIP		CHAR(10)		NULL,
DONATION_DATE CHAR(15) NULL,
DONATION_CURRENCY CHAR(20) NULL,
DONATION_ITEM VARCHAR(100) NULL,
DONATION_SERVICE VARCHAR(100) NULL,

PRIMARY KEY (D_CODE))